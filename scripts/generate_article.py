def get_unused_topic():
    """获取未使用的选题"""
    blog_dir = Path("src/content/articles")
    existing_slugs = set()
    
    if blog_dir.exists():
        for f in blog_dir.glob("*.md"):
            content = f.read_text(encoding='utf-8')
            match = re.search(r'slug:\s*(\S+)', content)
            if match:
                existing_slugs.add(match.group(1))
    
    available = [t for t in ARTICLE_TOPICS if t["slug"] not in existing_slugs]
    
    if not available:
        return None
    
    return random.choice(available)

